package Exerc046;

import java.util.Scanner;

public class Exerc046 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        var phaseOne = "Hello World";
        var phaseSecond = "I am a file";
        var phaseThird = "Read me until end-of-file";

        for (int i = 1; i <= 1; i++) {
            System.out.println(i + " " + phaseOne);

        }
        for (int i = 2; i <= 2; i++) {
            System.out.println(i + " " + phaseSecond);
        }
        for (int i = 3; i <= 3; i++) {
            System.out.println(i + " " + phaseThird);
        }

        sc.close();
    }
}
